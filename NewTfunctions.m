function [T]= NewTfunctions(a,c)
%
% T Functions
%
% inputs: c,a
%
sc=sqrt(1.-c^2);
ac=acos(c);
%
T0=c*sc*ac-(1.0-c*c);
T1=-1./3.*(2.+c^2)*sc+c*ac;
T2=c*(1.-c^2)-sc*(1.+c^2)*ac+c*ac^2;
T3=-(.125+c^2)*ac^2+.25*c*sc*ac*(7.+2.*c^2)...
   -.125*(1.-c^2)*(5.*c^2+4.);
T4=-ac+c*sc;
T5=-(1.-c^2)-ac^2+2.*c*sc*ac;
T6=T2;
T7=-(.125+c^2)*ac+.125*c*sc*(7.+2.*c^2);
T8=-sc*(2.*c^2+1.)/3.+c*ac;
p=-sc^3/3.;
T9=(-p+a*T4)/2.;
T10=sc+ac;
T11=ac*(1.-2.*c)+sc*(2.-c);
T12=sc*(2.+c)-ac*(2.*c+1.);
T13=(-T7-(c-a)*T1)/2.;
T14=1./16.+a*c/2.;
%
T15=T4+T10;
T16=T1-T8-(c-a)*T4+T11/2.;
T17=-2.*T9-T1+(a-0.5)*T4;
%
% store in a vector T: T(1) to T(18)
%
T(1)=T0;
T(2)=T1;
T(3)=T2;
T(4)=T3;
T(5)=T4;
T(6)=T5;
T(7)=T6;
T(8)=T7;
T(9)=T8;
T(10)=T9;
T(11)=T10;
T(12)=T11;
T(13)=T12;
T(14)=T13;
T(15)=T14;
T(16)=T15;
T(17)=T16;
T(18)=T17;